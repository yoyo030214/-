#!/bin/bash

# 显示欢迎信息
echo "========================================="
echo "    农业应用后端Docker部署脚本"
echo "========================================="

# 停止并移除旧的容器（如果存在）
echo "清理旧容器..."
docker-compose down

# 构建并启动服务
echo "构建并启动Docker服务..."
docker-compose up -d

# 显示容器状态
echo "容器状态："
docker-compose ps

echo "========================================="
echo "部署完成！服务已在后台运行"
echo "API服务地址: http://localhost:5000"
echo "数据库端口: 3307"
echo "=========================================" 